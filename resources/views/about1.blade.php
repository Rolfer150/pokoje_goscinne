    <h2 class="text-6xl font-bold p-4">O nas</h2>
    <p class="mt-10">Zapraszamy do wsi Kluszkowce w gminie Czorsztyn, w gorczańskim Paśmie Lubania. Miejscowość leży w
        malowniczej dolinie potoku Kluszkowianka, w pobliżu Jeziora Czorsztyńskiego i innych miejscowości turystycznych
        takich jak Czorsztyn, Niedzica, Krościenko nad Dunajcem i Szczawnica. Odległość do Nowego Targu to zaledwie
        23 km, a do Rabki-Zdrój 45 km. W naszym domowym zaciszu oferujemy pokoje 2-, 3- i 4-osobowe z łazienką na
        korytarzu oraz pokoje 2- i 3-osobowe z łazienkami. W każdym pokoju znajduje się telewizor. W dostępnych
        aneksach kuchennych można korzystać z naczyń, kuchenki i lodówki. Nasi goście mogą także grilować na terenie
        posesji. Teren domu jest ogrodzony, zapewniamy także miejsca parkingowe. Dla dzieci mamy atrakcje w postaci
        piaskownicy i huśtawki. Dzieci do lat 3 przebywają u nas za darmo, a dzieci do 10 lat – za połowę ceny.
        Ceny noclegów mieszczą się w przedziale 30 – 40 zł/osoba. Gwarantujemy miły wypoczynek w rodzinnej atmosferze
        i domowym zaciszu. Tutaj każdy poczuje się jak u siebie w domu. Dla turystów aktywnych polecamy kilka atrakcji,
        np. wycieczki szlakami turystycznymi wśród znakomitych krajobrazów oraz spływy Przełomem Dunajca. W Czorsztynie
        i Niedzicy warto zobaczyć dwa średniowieczne zamki. Nad jeziorem znajdują się wypożyczalnie sprzętu wodnego
        i przystanie żeglarskie. Polecamy rowery wodne i kajaki. Można wyruszyć w rejsy rekreacyjne statkami
        i gondolami. To tereny, które są idealne do uprawiania turystyki konnej. W pobliżu znajduje się także przejście
        graniczne ze Słowacją. Kluszkowce to świetna baza wypadowa w Pieniny, Gorce, Tatry i Beskid Sądecki.
        Pobyt w naszym domu to okazja do zwiedzania podhalańskich zabytków (np. kościół w Dębnie). W Kluszkowcach
        znajduje się ośrodek narciarski Czorsztyn Ski, na zboczach Góry Wdżar. W zimie proponuje oświetlone i naśnieżane
        stoki z wyciągami narciarskimi i wiele tras zjazdowych. Ośrodek działa także w lecie oferując wiele form
        aktywnego wypoczynku. Rowerzystów ucieszy Bike park z wypożyczalnią rowerów i nauką jazdy ekstremalnej,
        a na spokojne spacery polecamy ścieżki edukacyjne. W ośrodku znajduje się także tor saneczkowy – atrakcja dla
        całej rodziny. Zapraszamy serdecznie.</p>
